    private void AddBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBActionPerformed
        Publisher tmpPublisher = new Publisher();
        tmpPublisher = OracleConnect.doSelectPublisherByName(PublisherTF.getText());
        myBook.setId(Integer.parseInt(IdTF.getText()));
        myBook.setAuthor(AuthorTF.getText());
        myBook.setCid(CidTF.getText());
        myBook.setIsbn(IsbnTF.getText());
        myBook.setPrice(Double.parseDouble(PriceTF.getText()));
        myBook.setPublisher(tmpPublisher);
        myBook.setQuantity(Integer.parseInt(QuantityTF.getText()));
        myBook.setTitle(TitleTF.getText());
        myBook.setHistory("");
        
        if (OracleConnect.doAddBook(myBook)) {
            JOptionPane.showMessageDialog(null, "add a new book completed", "add book success", JOptionPane.INFORMATION_MESSAGE);
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "add a new book failed", "add book failed", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_AddBActionPerformed

    private void UpdateBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBActionPerformed
        myBook.setIsbn(IsbnTF.getText());
        myBook.setAuthor(AuthorTF.getText());
        myBook.setTitle(TitleTF.getText());
        myBook.setCid(CidTF.getText());
        myBook.setPrice(Double.parseDouble(PriceTF.getText()));
        myBook.setQuantity(Integer.parseInt(QuantityTF.getText()));
        myBook.setHistory(HistoryTA.getText());
        Publisher publisher = new Publisher();
        publisher = OracleConnect.doSelectPublisherByName(PublisherTF.getText());
        if (publisher != null) {
            myBook.setPublisher(publisher);
        }
        OracleConnect.doUpdateBook(myBook);
    }//GEN-LAST:event_UpdateBActionPerformed

