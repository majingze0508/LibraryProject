    public bookEditUI(Book book, String editType) {
        typeStr = editType;
        initComponents();
        if (book != null) {
            myBook = book;
            IdTF.setText(String.valueOf(book.getId()));
            IsbnTF.setText(book.getIsbn());
            TitleTF.setText(book.getTitle());
            AuthorTF.setText(book.getAuthor());
            PublisherTF.setText(book.getPublisher().getName());
            CidTF.setText(book.getCid());
            PriceTF.setText(String.valueOf(book.getPrice()));
            QuantityTF.setText(String.valueOf(book.getQuantity()));
            if (book.getHistory() != null && !book.getHistory().isEmpty()) {
                String[] history = book.getHistory().split(";");
                HistoryTA.setColumns(history.length);
                HistoryTA.setLineWrap(true);
                HistoryTA.setWrapStyleWord(true);
                for(String o: history) {
                    HistoryTA.append(o + "\n");
                }
            }
        }
        if (editType.equals("Update")) {
            AddB.setVisible(false);
            UpdateB.setVisible(true);
        }
        if (editType.equals("Add")) {
            AddB.setVisible(true);
            UpdateB.setVisible(false);
        }
        setVisible(true);
    }

