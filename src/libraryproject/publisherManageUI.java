    private void updateBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBActionPerformed
        String[] publisherContent = publisherList.getSelectedValue().toString().split("//");
        myPublisher.setPid(Integer.parseInt(publisherContent[0]));
        myPublisher.setName(publisherContent[1]);
        myPublisher.setAddress(publisherContent[2]);
        publisherEdit = new publisherEditUI(myPublisher, "Update");
    }//GEN-LAST:event_updateBActionPerformed

    private void deleteBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBActionPerformed
        String[] publisherContent = publisherList.getSelectedValue().toString().split("//");
        myPublisher.setPid(Integer.parseInt(publisherContent[0]));
        myPublisher.setName(publisherContent[1]);
        myPublisher.setAddress(publisherContent[2]);
        int result = JOptionPane.showConfirmDialog(null, "Do you want to delete this publisher", "Publiser Delete", JOptionPane.WARNING_MESSAGE);
        if (result == JOptionPane.OK_OPTION) {
            OracleConnect.doDeletePublisher(myPublisher);
        }
    }//GEN-LAST:event_deleteBActionPerformed

