    private void SearchBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBActionPerformed
        myPublisher = OracleConnect.doSelectPublisher(Integer.parseInt(SearchTF.getText()));
        if (myPublisher != null) {
            publisherEdit = new publisherEditUI(myPublisher, "Update");
        }
    }//GEN-LAST:event_SearchBActionPerformed
    
    private void GetPubliserList() {
        ArrayList<Publisher> publisers = new ArrayList<Publisher>();
        publisers = OracleConnect.doSelectAllPublisher();
        DefaultListModel model = new DefaultListModel();
        if (publisers != null && !publisers.isEmpty()) {
            
            String[] publisersInfo = new String[publisers.size()];
            for(int i = 0; i < publisersInfo.length; i++) {
                publisersInfo[i] = publisers.get(i).getPid() + "//" + publisers.get(i).getName() + "//" + publisers.get(i).getAddress();
                model.addElement(publisersInfo[i]);
            }
            publisherList.setModel(model);
        }
    }

