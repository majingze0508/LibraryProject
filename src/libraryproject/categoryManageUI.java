    private void GetCategoryList() {
        ArrayList<Category> categorys = new ArrayList<Category>();
        categorys = OracleConnect.doSelectAllCategory();
        DefaultListModel model = new DefaultListModel();
        if (categorys != null && !categorys.isEmpty()) {
            
            String[] categoryInfo = new String[categorys.size()];
            for(int i = 0; i < categoryInfo.length; i++) {
                categoryInfo[i] = categorys.get(i).getId() + "//" + categorys.get(i).getDesc();
                model.addElement(categoryInfo[i]);
            }
            CategoryList.setModel(model);
        }
    }
