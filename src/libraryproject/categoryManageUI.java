    private void deleteBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBActionPerformed
        String[] categoryContent = CategoryList.getSelectedValue().toString().split("//");
        myCategory.setId(categoryContent[0]);
        myCategory.setDesc(categoryContent[1]);
        int result = JOptionPane.showConfirmDialog(null, "Do you want to delete this category", "Category Delete", JOptionPane.WARNING_MESSAGE);
        if (result == JOptionPane.OK_OPTION) {
            OracleConnect.doDeleteCategory(myCategory);
        }
    }//GEN-LAST:event_deleteBActionPerformed

